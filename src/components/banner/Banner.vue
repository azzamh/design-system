<template>
  <div
    v-if="show"
    data-testid="banner"
    :class="classNames">
    <div
      class="banner__icon"
      data-testid="banner-icon"
      :class="{ 'banner__icon--custom' : $slots.icon }">
      <slot name="icon">
        <component
          :is="icon"
          v-if="icon" />
      </slot>
    </div>
    <div class="banner__body">
      <slot :close="close" />
    </div>
    <div
      v-if="dismissable"
      data-testid="banner-close"
      class="banner__close"
      @click="close()">
      <IconClose />
    </div>
  </div>
</template>

<script lang="ts">
import IconInfo from '@privyid/persona-icon/vue/information-circle-solid/20.vue'
import IconDanger from '@privyid/persona-icon/vue/exclamation-circle-solid/20.vue'
import IconClose from '@privyid/persona-icon/vue/close/16.vue'
import {
  defineComponent,
  PropType,
  ref,
  computed,
} from 'vue-demi'
import { StyleVariant } from '.'

const BannerIcons = {
  danger: IconDanger,
  info  : IconInfo,
}

export default defineComponent({
  components: {
    IconInfo,
    IconClose,
    IconDanger,
  },
  props: {
    variant: {
      type   : String as PropType<StyleVariant>,
      default: 'info',
    },
    dismissable: {
      type   : Boolean,
      default: true,
    },
  },
  emits: ['dismissed'],
  setup (props, { emit }) {
    const show = ref(true)

    const classNames = computed(() => {
      const result: string[] = ['banner']

      if (props.variant)
        result.push(`banner--${props.variant}`)

      return result
    })

    const icon = computed(() => {
      return BannerIcons[props.variant]
    })

    function close (): void {
      show.value = false
      emit('dismissed')
    }

    return {
      classNames,
      show,
      icon,
      close,
    }
  },
})
</script>

